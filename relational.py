n1=eval(input("Enter First Number :"))#10.11
n2=eval(input("Enter Second number :"))#10.12

print("{} > {} = {} ".format(n1,n2,n1>n2))#False
print("{} < {} = {} ".format(n1,n2,n1<n2))#True
print("{} >= {} = {} ".format(n1,n2,n1>=n2))#False
print("{} <= {} = {} ".format(n1,n2,n1<=n2))#True
print("{} == {} = {} ".format(n1,n2,n1==n2))#False
print("{} != {} = {} ".format(n1,n2,n1!=n2))#True
